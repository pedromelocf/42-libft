char	strrchr(const char *s, int c)
{
	size_t i;
	size_t *x;

	i = 0;
	x = (void *)0;
	while (s[i])
	{
		if (s[i] == c)
			x = &s[i];
		i++;
	}
	if (c == '\0')
		return (&s[i]);
	return (x);
}
