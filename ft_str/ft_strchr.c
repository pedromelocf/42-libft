#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] && c <= 126)
	{
		if (s[i] == c)
			return (char*)&s[i];
		i++;
	}
	if (c == '\0' || c >= 127)
		return (char*)&s[i];
	return ((void *)0);
}
