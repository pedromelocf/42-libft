int	ft_atoi(const char *nptr)
{
	size_t	i;
	size_t	c;

	c = 0;
	i = 0;
	while (nptr[i])
	{
		if ((nptr[i] >= '0') && (nptr[i] <= '9'))
			c = c * 10 + (size_t)nptr[i] - '0';
		else
			return (c);
		i++;
	}
	return (c);
}
